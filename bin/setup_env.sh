echo "SETUP OF ENV FILES"
if [ ! -f .env ]; then 
  printf "\e[31m.env FILE NOT FOUND\e[0m\n"
  exit 1
fi
MYSQL_PORT=3306
SERVER_DEV_PORT=3000
SERVER_PORT=80
source ./.env

# DB 
DB_PATH="./apps/db"
DB_PATH_SQL="${DB_PATH}/mysql.env"

> $DB_PATH_SQL
echo MYSQL_PORT=$MYSQL_PORT >> $DB_PATH_SQL
echo MYSQL_ROOT_PASSWORD=\"$MYSQL_ROOT_PASSWORD\" >> $DB_PATH_SQL
echo MYSQL_DATABASE=\"$MYSQL_DATABASE\" >> $DB_PATH_SQL

DB_ENV="${DB_PATH}/.env"
> $DB_ENV

DATABASE_URL="mysql://root:${MYSQL_ROOT_PASSWORD}@127.0.0.1:${MYSQL_PORT}/${MYSQL_DATABASE}"
echo MYSQL_PORT=$MYSQL_PORT >> $DB_ENV
echo DATABASE_URL=\"$DATABASE_URL\" >> $DB_ENV
echo MYSQL_DATABASE=\"$MYSQL_DATABASE\" >> $DB_ENV

# SERVER
SERVER_PATH="./apps/server/.env"
SERVER_PROD_ENV="${SERVER_PATH}.production"
SERVER_LOCAL_ENV="${SERVER_PATH}.local"

> $SERVER_LOCAL_ENV
> $SERVER_PROD_ENV

echo SERVER_PORT="$SERVER_DEV_PORT" > $SERVER_LOCAL_ENV
echo SERVER_PORT="$SERVER_PORT" > $SERVER_PROD_ENV

printf "\e[32mSUCCESS SETTING OF ENV\e[0m\n"