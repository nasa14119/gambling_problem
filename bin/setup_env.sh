MYSQL_DATABASE=gambling-problem

SERVER_DOMAIN=localhost
SERVER_DEV_PORT=3000
SERVER_PORT=80
CLIENT_PORT=3001
source ./.env

# DB 
DB_PATH="./apps/db"

DB_ENV="${DB_PATH}/.env"
> $DB_ENV
echo MYSQL_PORT=$MYSQL_PORT >> $DB_ENV
echo MYSQL_DATABASE=\"$MYSQL_DATABASE\" >> $DB_ENV
echo MYSQL_HOST=\"$MYSQL_HOST\" >> $DB_ENV
echo MYSQL_ROOT_PASSWORD=\"$MYSQL_ROOT_PASSWORD\" >> $DB_ENV


# SERVER
SERVER_PATH="./apps/server/.env"
SERVER_PROD_ENV="${SERVER_PATH}.production"
SERVER_LOCAL_ENV="${SERVER_PATH}.local"

> $SERVER_LOCAL_ENV
> $SERVER_PROD_ENV

echo SERVER_PORT="$SERVER_DEV_PORT" >> $SERVER_LOCAL_ENV
echo SERVER_PATH="http://$SERVER_DOMAIN:$CLIENT_PORT" >> $SERVER_LOCAL_ENV
echo JWT_SECRET="$JWT_SECRET" >> $SERVER_LOCAL_ENV
cat $DB_ENV >> $SERVER_LOCAL_ENV 

echo SERVER_PORT="$SERVER_PORT" >> $SERVER_PROD_ENV
echo SERVER_PATH="https://$SERVER_DOMAIN:$SERVER_PORT" >> $SERVER_PROD_ENV
echo JWT_SECRET="$JWT_SECRET" >> $SERVER_PROD_ENV
cat $DB_ENV >> $SERVER_PROD_ENV 

