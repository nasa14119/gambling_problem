type Suit = "c" | "d" | "h" | "s";
type Num =
  | "2"
  | "3"
  | "4"
  | "5"
  | "6"
  | "7"
  | "8"
  | "9"
  | "T"
  | "J"
  | "Q"
  | "K"
  | "A";
export type Card = `${Num}${Suit}`;

const suits: Record<Suit, number> = { c: 0, d: 1, h: 2, s: 3 };
const ranks: Record<Num, number> = {
  2: 0,
  3: 1,
  4: 2,
  5: 3,
  6: 4,
  7: 5,
  8: 6,
  9: 7,
  T: 8,
  J: 9,
  Q: 10,
  K: 11,
  A: 12,
};
