export const getCardHash = (card: Card) => {
  const [rank, suit] = card.split("") as [Num, Suit];
  return CARDS[suits[suit]][ranks[rank]];
};
// prettier-ignore
const rankNames = ["2","3","4","5","6","7","8","9","T","J","Q","K","A"];
const suitNames = [null, "s", "h", null, "d", null, null, null, "c"];
export const getCardName = (card: number): Card => {
  const rank = (card >>> 8) % 16;
  const suit = (card >>> 12) % 16;
  return `${rankNames[rank]}${suitNames[suit]}` as Card;
};
